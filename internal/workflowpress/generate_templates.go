// RunDurable executes the workflow over one input through the durable adapter. It
// is behavior-equivalent to Run (workflow.go): both delegate to the kernel
// Runner, so the durable and local paths cannot drift.
func RunDurable(ctx context.Context, in wp.RunInput) (*wp.RunResult, error) {
	spec, err := loadSpec()
	if err != nil {
		return nil, err
	}
	adapter, err := wp.NewLocalAdapter(spec)
	if err != nil {
		return nil, err
	}
	return adapter.Run(ctx, in)
}
