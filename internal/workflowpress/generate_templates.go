package workflowpress

// generate_templates.go holds the template bodies the Generator renders. They are
// kept out of generate.go so the generator logic stays small and the templates
// stay reviewable as a unit. Every template renders the deterministic specView
// (generate.go) and must avoid any non-deterministic input (time, randomness, map
// iteration). Maps are only ever read through the view's sorted accessors.
//
// The emitted Go files declare the workflow as DATA and a thin Run entrypoint
// that delegates to the kernel Runner — the generator never re-implements the
// state machine, it embeds the contract and points the kernel at it. That keeps
// the generated tool's behavior identical to the kernel's and to the durable
// adapter's.

// tmplWorkflow emits the generated package's entrypoint: the embedded canonical
// spec and a Run that decodes it and drives the kernel Runner. The generated
// package is self-contained except for the workflowpress kernel import.
const tmplWorkflow = `// Code generated by workflowpress.Generate; DO NOT EDIT.
//
// Workflow: {{.Spec.ID}} (version {{.Spec.Version}})
// Goal: {{.Spec.Goal}}
// Operator: {{.Spec.Operator}}
//
// This is the operator's generated internal tool. It embeds the frozen contract
// and runs it through the workflowpress kernel Runner — the same runtime the
// shipcheck replays and the durable adapter re-hosts, so all three paths behave
// identically. Actions run through the fail-closed Executor seam; external writes
// are gated and recorded, never performed live.
package {{.Package}}

import (
	"context"
	"encoding/json"
	"fmt"

	wp "github.com/nex-crm/wuphf/internal/workflowpress"
)

// specJSON is the frozen contract this tool was generated from, embedded verbatim
// so the runtime decodes the exact spec. It is emitted as a double-quoted,
// strconv.Quote-escaped Go string literal rather than a raw-string (backtick)
// literal: a raw-string literal terminates early on any backtick in the spec's
// free text (json.MarshalIndent does not escape backticks), which would break the
// generated Go. The quoted form survives any byte and decodes identically.
const specJSON = {{.SpecJSONLiteral}}

// loadSpec decodes the embedded contract. It is deterministic and cannot fail for
// the generated bytes; the error path exists only for defence in depth.
func loadSpec() (*wp.WorkflowSpec, error) {
	var spec wp.WorkflowSpec
	if err := json.Unmarshal([]byte(specJSON), &spec); err != nil {
		return nil, fmt.Errorf("{{.Spec.ID}}: decoding embedded spec: %w", err)
	}
	return &spec, nil
}

// Run executes the workflow over one input and returns the kernel RunResult: the
// ordered transition log plus the recorded (and gated) action calls. The host
// executor is fail-closed, so any external write is recorded as gated, never run.
func Run(ctx context.Context, in wp.RunInput) (*wp.RunResult, error) {
	spec, err := loadSpec()
	if err != nil {
		return nil, err
	}
	runner, err := wp.NewRunner(spec, wp.NewHostExecutor(), wp.DefaultGuardEvaluator{})
	if err != nil {
		return nil, fmt.Errorf("{{.Spec.ID}}: building runner: %w", err)
	}
	return runner.Run(ctx, in)
}
`

